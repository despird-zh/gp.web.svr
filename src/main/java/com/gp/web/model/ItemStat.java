package com.gp.web.model;

public class ItemStat {
	
	private String statText;
	
	private String statTooltip;

	public String getStatText() {
		return statText;
	}

	public void setStatText(String statText) {
		this.statText = statText;
	}

	public String getStatTooltip() {
		return statTooltip;
	}

	public void setStatTooltip(String statTooltip) {
		this.statTooltip = statTooltip;
	}	
	
}
